DROP DATABASE IF EXISTS Tienda_departamental;
create database Tienda_departamental;
USE Tienda_departamental;
create table cliente(
id_cliente int primary key auto_increment,
clave_cliente varchar(50) not null,
nombre varchar(100),
telefono varchar(15),
direccion text
);
create table Departamento(
id_departamento int primary key auto_increment,
clave_departamental varchar(50),
descripcion text
);
create table Vendedor(
id_vendedor int primary key auto_increment,
clave_vendedor varchar(100) not null,
nombre varchar(100),
rfc varchar(30) not null
);
CREATE TABLE proveedor (
id_proveedor INT AUTO_INCREMENT PRIMARY KEY,
nombre VARCHAR(100),
nombre_empresa VARCHAR(100),
telefono VARCHAR(15),
direccion VARCHAR(255)
);

create table Marca(
id_marca int primary key auto_increment,
nombre_marca varchar(50),
descripcion text
);
create table Articulo(
id_articulo int primary key auto_increment,
nombre text,
precio decimal(10,2),
existentes int(15),
id_departamento int(15),
id_marca int(15),
FOREIGN KEY (id_departamento) REFERENCES Departamento(id_departamento),
FOREIGN KEY (id_marca) REFERENCES Marca(id_marca)
);
CREATE TABLE Compras (
    id_compra INT AUTO_INCREMENT PRIMARY KEY,
    id_articulo INT,
    num_compras INT,
    fecha_compra DATE,
    FOREIGN KEY (id_articulo) REFERENCES Articulo(id_articulo)
);

CREATE TABLE ventas (
    id_venta INT AUTO_INCREMENT PRIMARY KEY,
    id_articulo INT,
    cantidad_ventas INT,
    id_cliente INT,
    fecha_producto DATE,
    FOREIGN KEY (id_articulo) REFERENCES Articulo(id_articulo),
    FOREIGN KEY (id_cliente) REFERENCES Cliente(id_cliente)
);

